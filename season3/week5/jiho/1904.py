# [1904번](https://www.acmicpc.net/problem/1904) 01 타일 (실버3)

# 1 - 1
# 2 - 00 11
# 3 - 001 100 111
# 4 - 0000 0011 1100 1001 1111
# 5 - 00001 00100 10000 00111 10011 11001 11111 11100

n = int(input())
dp = [0] * 1000001
dp[1] = 1
dp[2] = 2
for i in range(3, n+1):
    dp[i] = (dp[i-1] + dp[i-2]) % 15746
print(dp[n])
